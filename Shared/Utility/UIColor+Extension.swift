import UIKit

public extension UIColor {
    @nonobjc class var deepRed: UIColor {
        return UIColor(red: 155.0/255.0, green: 1.0/255.0, blue: 1.0/255.0, alpha: 1.0)
    }
    @nonobjc class var deepBlue: UIColor {
        return UIColor(red: 0.0/255.0, green: 100.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
}

